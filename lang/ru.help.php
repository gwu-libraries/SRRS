        <h4><a name="using_the_scheduler" id="using_the_scheduler"></a>Использование расписаний</h4>
        <p>Расписание - место, где Вы можете выполнить все функции планирования ресурсов.
           Понедельный показ начинается с текущей недели и простирается на 7 (по умолчанию) 
           дней. Здесь Вы можете просматривать расписания ресурсов, резервировать (заказывать) ресурсы, и
           править Ваши текущие заказы. Заказы помечены цветом и
           показываются все, но только <i>Ваши</i> заказы
           предоставляют ссылки на правку заказов. Все другие заказы
           предоставляют ссылки только для их просмотра.</p>
        <p>Вы можете изменить расписания (если их более одного), используя выпадающее
          меню в верхней части каждого расписания.</p>
        <p>Системный администратор может определять специфические периоды времени т.н. &quot;отключенное время&quot;,
          недоступные никому, кроме администратора. Заказы не могут быть
          размещены, если они конфликтуют с временами отключения.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="read_only" id="read_only"></a>Версия
        только-для-чтения</h5>
        <p>Если Вы еще не зарегистрированны или не авторизованы, Вы можете просматривать версию
          только-для-чтения любого расписания, нажав на ссылку &quot;Расписание только-для-чтения&quot; 
          на странице авторизации. Эта версия системы показывает Вам все ресурсы
          и заказы, но Вы не можете посмотреть какую-либо подробную информацию об
          этом, равно как и разместить заказы.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="making_a_reservation" id="making_a_reservation"></a>Создание
          заказа</h5>
        <p>Для резервирования (заказа) ресурса, сначала перейдите в таблице на день, в котором
          Вы хотите сделать заказ. Как только вы попали в такую таблицу
          дня заказа, нажмите на имени ресурса. Появится всплывающее окно,
           где Вы можете выбрать дни начал и окончания (если позволяется) и
          время, на которое Вы хотите зарезервировать (заказать) выбранный 
          ресурс.</p>
        <p>После выбора времени появится сообщение о том, какой
          продолжительности заказ для данного ресурса возможен. Если Ваш заказ
          длиннее или короче, чем эти допустимые интервалы времени, то заказ не будет принят.</p>
        <p>Вы можете также, если хотите, повторить заказ. Для повторения
          заказа выберите дни, на которые Вы хотите сделать повтор, затем выберите
          длительность повторения заказа. Заказ
          будет сделан для начального выбраного дня и для всех дней, которые Вы
          выбрали для повтора.Все дни, которые не могут быть зарезервированы из-за
        конфликтов заказов будет выведены списком. Если Вы создаете мультидневной
          заказ, то повторяющиеся опции не будут доступны.</p>
        <p>Вы можете добавить свод по этому заказу, пометив поле свода
          заказа. Этот свод будет в дальнейшем доступен для чтения для всех
          других пользователей.</p>        
        <p>После установки корректных дней/часов начала и окончания для данного
           заказа и выбора, если необходимо, повтора заказа, нажмите на кнопку
          &quot;Сохранить&quot;.
           Если резервирование не удалось, то появится сообщение, информирующее
           Вас о днях, когда заказ не удался. Если заказа неуспешен, вернитесь
           назад и отредактируйте запрашиваемое время, так, чтобы оно
           не перекрывало другие текущие 
          заказы. После успешного резервирования, расписание
            будет автоматически обновлено. Обязательно будет перезагружена вся
            информация о заказах из БД.</p>
        <p>Вы не можете делать заказ для даты, которая уже прошла, для ресурса,
           на который у Вас нет прав использования или для ресурса, который
          деактивирован в настоящее время. Такие ресурсы будут подсвечены серым и
          не имеют ссылки на заказывание.</p>
