        <h5><a name="modifying_deleting_a_reservation" id="modifying_deleting_a_reservation"></a>Правка/Удаление 
          заказа</h5>
        <p>Существует много способов правки или удаления заказа. Один способ -
          со страницы <a href="#my_control_panel">Моя
             панель управления</a> как описано выше. Другой способ - через расписание
              он-лайн. Как замечено выше, Вы можете править только Ваши
             заказы. Все другие заказы показываются, но не имеют ссылки
              для их редактирования.</p>
        <p>Для редактирования заказа через расписание, просто нажмите на
          заказ, который Вы хотите изменить. Это выведет всплывающее окно,
          подобное окну заказа. У Вас имеется две возможности: 
          1) Вы можете изменить время начала или окончания заказа,
          или 2) Вы можете нажать на метку &quot;Удаление&quot;.
           После совершения Ваших изменений, нажмите на кнопку &quot;Править&quot; 
          в нижней части формы. Ваши новые опции будут оценены на
           соответствие текущим заказам и появившееся сообщение позволит Вам
          узнать статус Вашей модификации. Если Вы хотите изменить время, то вернитесь
           в окно модификации и выберите новое время, которое не перекрывает
           другие заказы. После того, как Ваш заказ будет успешно модифицирован,
           расписание будет автоматически обновлено. Будет обязательно перезагружена
           вся информация о заказах из БД.</p>
        <p>Для модификации группы повторяющихся заказов, выставьте флажок
        &quot;Обновить все повторяющиеся записи в группе?&quot;. Все конфликтные даты
          будут показаны списком.</p>
        <p>Вы не можете править заказ в день, который уже прошел.</p>
