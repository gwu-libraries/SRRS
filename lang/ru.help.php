          в которых Вы участвуете. Это не показывает заказы,
          которые Вы создали. В этой таблице Вы можете завершить Ваше 
          участие в некоторых заказах. Если Вы откажетесь от участия,
          Вы не сможете участвовать в таком заказе, пока создатель
          заказа не пригласит Вас
          вновь.</p>
