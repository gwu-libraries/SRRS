        <h5><a name="quick_links" id="quick_links"></a>Мои быстрые ссылки</h5>
        <p>Блок быстрых ссылок предоставляет ссылки на общие приложения.
          Во-первых, &quot;Перейти на расписание он-лайн&quot; переводит Вас на
          расписание по умолчанию. Здесь Вы можете просматривать ресурсы расписаний, резервировать
          ресурсы (заказывать их), и модифицировать Ваши текущие заказы.</p>
        <p>&quot;Смотреть мой календарь&quot; переносит Вас на просмотр календаря заказов
          которые Вы запланировали или в которых участвуете. Это можно просматривать
          по дням, неделям или месяцам.</p>
        <p>&quot;Смотреть плановый &amp; ресурсный календарь&quot; переносит Вас на
          просмотр календаря заказов для выбранного ресурса или всех ресурсов
          выбранного расписания. Если Вы выбрали подневный просмотр специфического
          ресурса, Вы также сможете распечатать a &quot;Подписанную таблицу&quot; при
          помощи нажатия на иконку с записной книжкой вслед за выпадающим меню ресурсов.</p>
        <p>&quot;Измененить мою профильную информацию/пароль &quot; переводит на
          страницу позволяющую Вам редактировать Вашу персональную информацию, такую как
          адрес e-mail авторизации, имя, номер телефона и пароль. Вся эта информация будет
          выведена на экран для Вас. Пустые или неизменяемые значения не будут изменяться.</p>
        <p>&quot;Управление моими установками E-mail&quot; перенесёт Вас на страницу, где
          Вы можете изменить когда и как Вы хотите проводить такие контакты, относительно Вашего
          использования расписаний. По умолчанию, Вы будете получать по e-mail предупреждения HTML-формата всякий
          раз, как Вы добавляете, правите или удаляете заказ.</p>
        <p>Последняя ссылка, &quot;Выход&quot; завершает Вашу текущую
          рабочую сессию и возвращает Вас на экран авторизации.</p>
