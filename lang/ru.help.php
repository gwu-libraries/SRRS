        <h4><a name="getting_started" id="getting_started"></a>Введение</h4>
        <p>Для использования системы phpScheduleIt, Вы должны первым делом зарегистрироваться. 
          Если Вы уже зарегистрированы, то Вы должны авторизоваться, прежде чем использовать
          систему. В верхней части каждой страницы (кроме страниц регистрации и
          авторизации) Вы можете видеть приглашение, сегодняшнюю дату, и небольшие ссылки
          -- &quot;Выйти&quot; и &quot;Моя панель управления&quot;
          под приглашением, и ссылку &quot;Помощь&quot под
          датой.</p>
          <p>Если предыдущий пользователь высвечивается в приглашении, нажмите на &quot;Выйти 
          &quot; для очистки использованных куки и <a href="#logging_in">Авторизуйтесь 
          </a> сами. Нажатие на ссылку &quot;Моя панель управления&quot; отправляет 
          Вас на <a href="#my_control_panel">Вашу панель управления</a>, Вашу &quot;домашнюю 
          страницу&quot; расписания.
          Нажатие на ссылку &quot;Помощь&quot; приводит к появлению всплывающего окна помощи. Нажатие
          на ссылку &quot;E-mail администратора&quot; открывает новое письмо к системному
          администратору.</p>
          <p><font color="#FF0000">Предупреждение:</font> Если у Вас запущен пакет Norton Personal
            Firewall во время использования системы phpScheduleIt, Вы можете иметь проблемы.
            Пожалуйста, блокируйте Norton Personal Firewall во-время использования системы phpScheduleIt
            и включите его вновь после окончания использования данной системы.</p>
          <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="registering" id="registering"></a>Регистрация</h5>
        <p>Для регистрации перейдите на страницу регистрации. Этого можно достигнуть 
          переходом по ссылке на начальной странице авторизации. Вы должны заполнить все поля. 
          Адрес e-mail Вашей регистрации станет Ващим логином. Информация, 
          которую Вы введете, может быть изменена в любое время <a href="#quick_links">изменением 
          Вашего профиля</a>. При выборе опции &quot;Сохранить мою авторизацию&quot; будут 
          использоваться куки для идентификации Вас всякий раз, как Вы будете возвращаться в расписание, 
          обходя необходимость авторизоваться каждый раз. <i>Используйте эту 
          опцию только, если Вы единственый пользователь расписания на Вашем компьютере.</i> 
          После регистрации, Вы будете перенаправлены на <a href="#my_control_panel">Вашу 
          панель управления</a>.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="logging_in" id="logging_in"></a>Авторизация</h5>
        <p>Авторизация проста после ввода Вами e-mail и пароля. 
          Вы должны <a href="#registering">зарегистрироваться</a> до авторизации. 
          Это может быть выполнено, следуя ссылке на регистрацию на странице 
          авторизации. Выбор опции &quot;Сохранить авторизацию&quot; использует 
          куки (cookies) для Вашей идентификации, каждый раз, как Вы возвращаетесь к расписаннию, избавляя 
          от необходимости авторизоваться каждый раз вновь. <i>Используйте эту опцию только, если 
          Вы единственый пользователь расписания на Вашем компьютере.</i> После 
          авторизации, Вы будете переадресованы на страницу <a href="#my_control_panel">Моя 
          панель управления</a>.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="language" id="language"></a>Выбор моего языка</h5>
        <p>На странице авторизации имеется выпадающее меню со всеми доступными
          языковыми переводами, которые включил Ваш администратор <a href="#my_control_panel"></a>.
          Пожалуйста, выберите нужный Вам язык и все тексты системы phpScheduleIt
          будут переведены. Это не переводит тесксты, которые ввел Ваш
          администратор или другие пользователи; это только переводит текст
          приложения. Вы должены выйти из системы, чтобы
          выбрать другой язык.</p>
        <p align="right"><a href="#top">Вверх</a></p>        
        <h5><a name="manage_profile" id="manage_profile"></a>Изменение профильной информации или пароля</h5>
        <p>Чтобы изменить Вашу профильную информацию (имя, e-mail и т.п.) или Ваш пароль, 
          первым делом авторизуйтесь в системе. На странице <a href="#my_control_panel">Моя панель 
          управления</a>, в блоке <a href="#quick_links">Мои быстрые ссылки</a>, нажмите на &quot;Изменение 
          моей профильной информации/пароля&quot;. Это переправит Вас на форму 
          заполненную Вашей информацией. Редактируйте любую информацию по Вашему желанию. Поля 
          слева не изменяются. Если Вы хотите изменить Ваш пароль, 
          введите его дважды. После модификации Вашей информации, нажмите &quot;Править профиль&quot; 
          и Ваши изменения будут сохранены в БД. Вы вернетесь после этого на страницу 
          Моя панель управления.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="resetting_password" id="resetting_password"></a>Переустановка забытого пароля</h5>
        <p>Если Вы забыли Ваш пароль, Вы можете переустановить его и получить новое 
          письмо e-mail для Вас. Чтобы это сделать, перейдите на страницу авторизации и нажмите 
          на ссылку &quot;Я забыл мой пароль&quot; в нижней части формы авторизации. 
          Вы будете переправлены на новую страницу, и Вам будет предложено ввести Ваш адрес e-mail. 
          После нажатия на ссылку &quot;Подтвердить&quot;, будет создан новый, случайно сгенерированный 
          пароль. Этот новый пароль будет установлен в БД и сообщен по e-mail 
          Вам. После получения этого e-mail, пожалуйста скопируйте и вставьте Ваш новый пароль, 
          <a href="#logging_in">авторизуясь</a> с ним, и быстро <a href="#manage_profile">измените 
          Ваш пароль</a>.</p>
