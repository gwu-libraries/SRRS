        <h5><a name="my_training" id="my_training"></a>Мои разрешения</h5>
        <p>Таблица Мои разрешения показывает все ресурсы, на которые Вы имеете разрешение к их использованию.
		  Перечисляются имена ресурсов, их местоположение и номер телефона, по которому Вы 
          можете позвонить для контакта с их администрацией.</p>
        <p>По завершению регистрации, Вы не имеете прав на использование каких-либо ресурсов, если администратор
		  не решит предоставлять пользователям такие права автоматически.  Администратор - единственный, кто может дать Вам
		  разрешение на использование какого-либо ресурса. Вам не будет позволено зарезервировать (заказать) ресурс на 
          который у Вас нет разрешения, но Вы можете просматривать его расписание 
          и текущие заказы.</p>
        <p align="right"><a href="#top">Вверх</a></p>
        <h5><a name="my_invitations" id="my_invitations"></a>Мои приглашения</h5>
        <p>Таблица Мои приглашения показывает все заказы, на которые Вы приглашены
          к участию и позволяет Вам принять или отклонить участие
          в таком заказе. Если Вы принимаете приглашение, Вы имеете ещё возможность
          отклонить Ваше участие позднее. Если Вы отклоняете приглашение, Вы
          не сможете его принять, пока создатель заказа не пригласит Вас
          вновь.</p>
        <p align="right"><a href="#top">Вверх</a></p>        
