        <h5><a name="my_reservations" id="my_reservations"></a>Мои заказы</h5>
        <p>Блок Мои заказы показывает все Ваши предстоящие заказы, начатые 
          сегодня (по умолчанию). Эта таблица перечисляет все даты заказов, 
          ресурсы, Дату/Время их создания, Дату/Время их последней модификации, 
          время начала и время окончания. Из этого блока Вы также можете модифицировать какой-либо заказ 
          или удалить его, просто нажав на ссылку &quot;Править&quot; или &quot;Удалить&quot; 
          в конце соответствующей строки заказа. Обе эти опции 
          выводят всплывающее окошко, где Вы можете подтвердить Ваши изменения заказа. 
          Нажатие на дату заказа выводит новое окно, где Вы 
          можете посмотреть подробную информацию о соответствующем заказе.</p>
        <p>Чтобы отсортировать Ваши заказы по специфическому столбцу, нажмите на ссылку с &#150; 
          или + в верхней части такого столбца. Знак минус будет сортировать Ваши заказы 
          в убывающем порядке в соответствии с именем столбца, знак плюс будет сортировать Ваши 
          заказы в возрастающем порядке в соответствии с именем столбца.</p>
